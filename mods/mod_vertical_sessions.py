    def calc_spiral(ws_=9):
        x = y = 0
        dx = 0
        dy = -1
        list_ = []
        for i in range(ws_ ** 2):
            if (-ws_ / 2 < x <= ws_ / 2) and (-ws_ / 2 < y <= ws_ / 2):
                list_.append((x, y))
            if x == y or (x < 0 and x == -y) or (x > 0 and x == 1 - y):
                dx, dy = -dy, dx
            x, y = x + dx, y + dy
        return list_

    def update_painter(self, dic_):
        self.dic_d = dic_['d']
        self.dic_c = dic_['c']
        self.update()



            pen.setBrush(QColor(255, 0, 0, 100))
            painter.setPen(pen)
            z_ = self.dic_t['Z_min'] + (1 - self.mp_.y() / self.height()) * (self.dic_t['Z_max'] - self.dic_t['Z_min'])
            painter.drawText(self.mp_.x() + 15, self.mp_.y(), str(round(z_, 3)))

